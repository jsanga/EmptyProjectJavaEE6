package com.jscompany.ebsystem.entidades.usuarios;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToOne;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import org.hibernate.annotations.FilterDef;

/**
 *
 * @author Joao Sanga
 */
@Entity
@Table(name = "loguin", schema = "usuarios")
@FilterDef(name = "activos", 
    defaultCondition = "estado = 'TRUE'")
@NamedQueries({
    @NamedQuery(name = "Loguin.findAll", query = "SELECT l FROM Loguin l")})
public class Loguin implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    @Size(max = 100)
    @Column(name = "username")
    private String username;
    @Size(max = 50)
    @Column(name = "pass")
    private String pass;
    @Column(name = "estado")
    private Boolean estado;
    @OneToOne(mappedBy = "loguin", fetch = FetchType.LAZY)
    private Persona persona;

    public Loguin() {
    }

    public Loguin(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public Boolean getEstado() {
        return estado;
    }

    public void setEstado(Boolean estado) {
        this.estado = estado;
    }

    public Persona getPersona() {
        return persona;
    }

    public void setPersona(Persona persona) {
        this.persona = persona;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Loguin)) {
            return false;
        }
        Loguin other = (Loguin) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.jscompany.ebsystem.entidades.usuarios.Loguin[ id=" + id + " ]";
    }
    
}
